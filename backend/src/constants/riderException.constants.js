/** Rider operational exception types — never auto-cancel orders. */
const RIDER_EXCEPTION_TYPES = Object.freeze({
  DELAYED_VEHICLE: 'DELAYED_VEHICLE',
  COLD_CHAIN_ISSUE: 'COLD_CHAIN_ISSUE',
  NEED_ASSISTANCE: 'NEED_ASSISTANCE',
});

const RIDER_EXCEPTION_LABELS = Object.freeze({
  DELAYED_VEHICLE: 'Delayed (vehicle issue)',
  COLD_CHAIN_ISSUE: 'Cold Chain Issue',
  NEED_ASSISTANCE: 'Need Assistance',
});

const RIDER_OPERATIONAL_STATUS = Object.freeze({
  NORMAL: 'normal',
  DELAYED_VEHICLE: 'delayed_vehicle',
  COLD_CHAIN_ISSUE: 'cold_chain_issue',
  NEED_ASSISTANCE: 'need_assistance',
});

const EXCEPTION_TO_OPERATIONAL_STATUS = Object.freeze({
  [RIDER_EXCEPTION_TYPES.DELAYED_VEHICLE]: RIDER_OPERATIONAL_STATUS.DELAYED_VEHICLE,
  [RIDER_EXCEPTION_TYPES.COLD_CHAIN_ISSUE]: RIDER_OPERATIONAL_STATUS.COLD_CHAIN_ISSUE,
  [RIDER_EXCEPTION_TYPES.NEED_ASSISTANCE]: RIDER_OPERATIONAL_STATUS.NEED_ASSISTANCE,
});

const RIDER_EXCEPTION_TYPE_SET = new Set(Object.values(RIDER_EXCEPTION_TYPES));

/** Order statuses from which a rider may report an operational exception. */
const RIDER_EXCEPTION_ELIGIBLE_STATUSES = new Set([
  'OUT_FOR_DELIVERY',
  'PICKED_UP',
  'ON_THE_WAY',
  'RIDER_NEARBY',
]);

module.exports = {
  RIDER_EXCEPTION_TYPES,
  RIDER_EXCEPTION_LABELS,
  RIDER_OPERATIONAL_STATUS,
  EXCEPTION_TO_OPERATIONAL_STATUS,
  RIDER_EXCEPTION_TYPE_SET,
  RIDER_EXCEPTION_ELIGIBLE_STATUSES,
};
