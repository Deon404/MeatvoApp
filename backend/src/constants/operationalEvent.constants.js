const resolveActorType = (actorRole) => {
  const role = String(actorRole || '').toLowerCase();
  if (role === 'admin') return ACTOR_TYPES.ADMIN;
  if (role === 'delivery' || role === 'rider' || role === 'delivery_partner') {
    return ACTOR_TYPES.RIDER;
  }
  if (role === 'staff' || role === 'kitchen') return ACTOR_TYPES.STAFF;
  if (role === 'customer') return ACTOR_TYPES.CUSTOMER;
  return ACTOR_TYPES.SYSTEM;
};

